/*
 * data.js — Single source of truth for the UNR cancer-proximity faculty site.
 * ---------------------------------------------------------------------------
 * Everything the site renders (the ranking, the faculty cards, the cross-linked
 * bibliography) is derived from the two structures defined here:
 *
 *   SOURCES  — every citable reference, keyed by a short id.
 *   FACULTY  — every profiled faculty member, citing SOURCES by id.
 *
 * Design notes (kept in sync with README.md > "Data model"):
 *   - Each faculty `evidence` and each student `sources` entry is a SOURCES key.
 *     test/validate.js fails on a dangling id (cited but undefined) and on an
 *     unused source, except those flagged `structural: true` (site-wide
 *     directory links that are referenced from the page chrome, not a card).
 *   - `tier` drives grouping; `score` (0–10) drives ordering within the ranking.
 *     The rubric that produces `score` lives in methodology.html and is
 *     summarized in scoreRubric() below so code and docs cite each other.
 *   - No student is listed without at least one verifiable public source. Where
 *     nothing is documented, `students` is a plain string explaining that.
 *
 * Exposed as globals (window.SOURCES / window.FACULTY) rather than ES modules so
 * the site also works when index.html is opened directly from disk (file://).
 */

